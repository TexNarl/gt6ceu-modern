    private final String id;
    private final Block block;
    private final int meta;
    private final int chanceMinusOne;
    private final int divider;

    public GtWorldgenPit(String id, Block block, int meta, int chance, int divider) {
        this.id = id;
        this.block = block;
        this.meta = meta;
        this.chanceMinusOne = Math.max(0, chance - 1);
        this.divider = Math.max(1, divider);
    }

    @Override
    public String id() {
        return id;
    }

    @Override
    public boolean supports(ServerLevel level) {
        return level.dimension() == ServerLevel.OVERWORLD;
    }

    @Override
    public void generate(ServerLevel level, ChunkAccess chunk, RandomSource random) {
        if (random.nextInt(divider) > chanceMinusOne) {
            return;
        }
        BlockPos center = chunk.getPos().getMiddleBlockPosition(level.getSeaLevel());
        Biome biome = level.getBiome(center).value();
        if (!level.getBiome(center).is(BiomeTags.IS_PLAINS) && !level.getBiome(center).is(BiomeTags.IS_SAVANNA)) {
            return;
        }
        if (!GtWorldgenClimate.isTemperate(biome) || GtWorldgenClimate.isCold(biome) || GtWorldgenClimate.isArid(biome)) {
            return;
        }
        int minX = chunk.getPos().getMinBlockX();
        int minZ = chunk.getPos().getMinBlockZ();
        int baseX = minX - 16;
        int baseZ = minZ - 16;
        int seaLevel = level.getSeaLevel();
        int upperBound = seaLevel + 16;
        int lowerBound = seaLevel - 8;

        for (int i = 0; i < 48; i++) {
            for (int j = 0; j < 48; j++) {
                if (!SHAPE[i][j]) {
                    continue;
                }
                BlockPos lastPos = new BlockPos(baseX + i, upperBound + 1, baseZ + j);
                BlockState lastState = level.getBlockState(lastPos);
                int generated = 0;
                for (int y = upperBound; y >= lowerBound && generated < 7; y--) {
                    BlockPos pos = new BlockPos(baseX + i, y, baseZ + j);
                    BlockState state = level.getBlockState(pos);
                    if (isTargetState(state)) {
                        generated++;
                        lastState = state;
                        continue;
                    }
                    if (!state.isSolidRender(level, pos)) {
                        if (generated > 0) {
                            break;
                        }
                        lastState = state;
                        continue;
                    }
                    if (isSoftBlock(state)) {
                        if (generated <= 0 && isTreeCover(lastState)) {
                            lastState = state;
                            continue;
                        }
                    } else if (generated > 0 && !state.is(BlockTags.BASE_STONE_OVERWORLD) && !state.is(BlockTags.BASE_STONE_NETHER)) {
                        break;
                    } else if (generated <= 0) {
                        lastState = state;
                        continue;
                    }
                    level.setBlock(pos, targetState(), 2);
                    generated++;
                    lastState = state;
                }
            }
        }
    }

    private boolean isTargetState(BlockState state) {
        if (!state.is(block)) {
            return false;
        }
        if (block instanceof GtMetaBlock) {
            return state.getValue(GtMetaBlock.META) == meta;
        }
        return true;
    }

    private BlockState targetState() {
        BlockState state = block.defaultBlockState();
        if (block instanceof GtMetaBlock) {
            return state.setValue(GtMetaBlock.META, meta);
        }
        return state;
    }

    private boolean isSoftBlock(BlockState state) {
        return state.is(Blocks.DIRT)
                || state.is(Blocks.GRASS_BLOCK)
                || state.is(Blocks.SAND)
                || state.is(Blocks.CLAY)
                || state.is(Blocks.GRAVEL);
    }

    private boolean isTreeCover(BlockState state) {
        return state.is(BlockTags.LOGS) || state.is(BlockTags.LEAVES) || state.is(BlockTags.SAPLINGS);
    }


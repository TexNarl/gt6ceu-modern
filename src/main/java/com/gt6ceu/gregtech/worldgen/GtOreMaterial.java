    GtOreMaterial(int id, String registryName) {
        this.id = id;
        this.registryName = registryName;
    }

    public int getId() {
        return id;
    }

    public String getRegistryName() {
        return registryName;
    }

    public static int maxId() {
        return Arrays.stream(values()).mapToInt(GtOreMaterial::getId).max().orElse(0);
    }

    public static GtOreMaterial byId(int id) {
        for (GtOreMaterial material : values()) {
            if (material.id == id) {
                return material;
            }
        }
        return EMPTY;
    }
